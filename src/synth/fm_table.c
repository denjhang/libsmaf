/* --- Wave table builders --- */

static void build_wave_sine(void) {
    for (int i = 0; i < WAVE_SIZE; i++)
        fm_wave_sine[i] = (int16_t)(sin(2.0 * PI * i / WAVE_SIZE) * 32767.0);
}

static void build_wave_half(void) {
    for (int i = 0; i < 512; i++) {
        fm_wave_half[i] = fm_wave_sine[i];
        fm_wave_half[512 + i] = 0;
    }
}

static void build_wave_abs(void) {
    for (int i = 0; i < 512; i++) {
        fm_wave_abs[i] = fm_wave_sine[i];
        fm_wave_abs[512 + i] = fm_wave_sine[i];
    }
}

static void build_wave_square(void) {
    for (int i = 0; i < 512; i++) {
        fm_wave_square[i] = 32767;
        fm_wave_square[512 + i] = -32768;
    }
}

static void build_wave_saw(void) {
    for (int i = 0; i < 512; i++) {
        fm_wave_saw[i] = (int16_t)(32767.0 * i / 512.0);
        fm_wave_saw[512 + i] = (int16_t)(32767.0 * i / 512.0 - 32767.0);
    }
}

static void build_wave_tri(void) {
    for (int i = 0; i < 256; i++) {
        fm_wave_tri[i] = (int16_t)(32767.0 * i / 256.0);
        fm_wave_tri[256 + i] = (int16_t)(32767.0 * (256.0 - i) / 256.0);
        fm_wave_tri[512 + i] = (int16_t)(-32767.0 * i / 256.0);
        fm_wave_tri[768 + i] = (int16_t)(-32767.0 * (256.0 - i) / 256.0);
    }
}

static void build_wave_exp(void) {
    for (int i = 0; i < 512; i++) {
        double x = i * 16.0 / 256.0;
        fm_wave_exp[i] = (int16_t)(pow(2.0, -x) * 32767.0);
        fm_wave_exp[1023 - i] = (int16_t)(-pow(2.0, -(x + 1.0 / 16.0)) * 32767.0);
    }
}

/* Sine wave returned by default */
const int16_t *smaf_fm_get_wave(int ws) {
    switch (ws) {
        case 1: return fm_wave_half;
        case 2: return fm_wave_abs;
        case 3: return fm_wave_square;
        case 4: return fm_wave_exp;
        case 5: return fm_wave_tri;
        case 6: return fm_wave_saw;
        default: return fm_wave_sine;
    }
}

/* Build note-to-phase table */
static void build_note_table(uint32_t sample_rate) {
    double base = 440.0;
    for (int n = 0; n < 128; n++) {
        double freq = base * pow(2.0, (n - 69) / 12.0);
        /* phase_inc = freq * 2^32 / sample_rate, Q32 representing one full cycle */
        note_phase_table[n] = (uint32_t)(freq * 4294967296.0 / sample_rate);
    }
}

void smaf_synth_init_tables(uint32_t sample_rate) {
    build_wave_sine();
    build_wave_half();
    build_wave_abs();
    build_wave_square();
    build_wave_saw();
    build_wave_tri();
    build_wave_exp();
    build_note_table(sample_rate);

    /* Copy KSL table */
    memcpy((void *)smaf_ksl_table, ksl_data, sizeof(ksl_data));

    /* Build pan table: constant-power panning using cos/sin law */
    for (int i = 0; i < 128; i++) {
        double a = PI * 0.5 * (double)i / 127.0;
        smaf_pan_table[i][0] = (int16_t)(cos(a) * 32767.0);
        smaf_pan_table[i][1] = (int16_t)(sin(a) * 32767.0);
    }

    /* Build DT table: convert Hz offsets to phase increments */
    for (int dt = 0; dt < 8; dt++) {
        for (int ksn = 0; ksn < 16; ksn++) {
            /* Store Hz offset as Q8 */
            smaf_dt_table[dt][ksn] = dt_hz[dt][ksn];
        }
    }
}

const int16_t *smaf_get_sine_table(void) {
    return fm_wave_sine;
}

uint32_t smaf_get_note_phase(uint8_t note) {
    if (note >= 128) note = 127;
    return note_phase_table[note];
}
